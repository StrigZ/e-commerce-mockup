import { Item } from "./types.ts";

export function getGenerateDescriptionPrompt(item: Item): string {
    const { title, price, category, params } = item;
    const paramsText = Object.entries(params)
        .map(([key, value]) => `${key}: ${value}`)
        .join(', ');

    return `Ты — опытный копирайтер на площадке объявлений о продаже б/у и почти новых товаров (аналог Авито).

Напиши продающее описание для объявления на основе следующих данных:
Категория: ${category}
Название: ${title}
Цена: ${price} руб.
Характеристики: ${paramsText}

Требования:
- Описание должно быть на русском языке
- Длина: 3-5 предложений
- Тон: дружелюбный, но профессиональный
- Упомяни состояние товара, если оно указано в характеристиках
- Подчеркни ключевые преимущества товара
- Не придумывай характеристики, которых нет в данных
- Верни только текст описания, без заголовков и пояснений`;
}

