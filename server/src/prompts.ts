import { Item } from "./types.ts";

export function getGenerateDescriptionPrompt(item: Item) {
   const { title, price, category, params } = item
   return `Ты — опытный копирайтер на площадке объявлений. Напиши продающее описание для объявления на основе следующих данных:

Категория: ${category}
Название: ${title}
Цена: ${price} руб.
Характеристики: ${Object.entries(params).map(([key, value]) => `${key}: ${value}`).join(', ')}

Требования:
- Описание должно быть на русском языке
- Длина: 3-5 предложений
- Тон: дружелюбный, но профессиональный
- Подчеркни ключевые преимущества товара
- Не придумывай характеристики, которых нет в данных
- Верни только текст описания, без заголовков и пояснений`
}

export function getImproveDescriptionPrompt(item: Item) {
   const { title, price, category, params, description } = item
   return `Ты — опытный копирайтер на площадке объявлений. Улучши существующее описание объявления, сделав его более продающим.

Категория: ${category}
Название: ${title}
Цена: ${price} руб.
Характеристики: ${Object.entries(params).map(([key, value]) => `${key}: ${value}`).join(', ')}
Текущее описание: ${description}

Требования:
- Описание должно быть на русском языке
- Длина: 3-5 предложений
- Тон: дружелюбный, но профессиональный
- Сохрани фактическую информацию из исходного описания
- Не придумывай характеристики, которых нет в данных
- Верни только текст описания, без заголовков и пояснений`
}

export function getMarketPricePrompt(item: Item): string {
   const { title,  category, params,description } = item;
   const paramsText = Object.entries(params)
      .map(([key, value]) => `${key}: ${value}`)
      .join(', ');

  return `Ты — эксперт по оценке стоимости товаров на российских площадках объявлений (Авито, Юла).

Оцени рыночную стоимость товара на основе следующих данных:
Категория: ${category}
Название: ${title}
Характеристики: ${paramsText}
Описание: ${description}

Требования:
- Ответ должен быть на русском языке
- Укажи рекомендуемый диапазон цен в формате "X — Y руб."
- Объясни кратко, от чего зависит цена в данном случае
- Длина ответа: 2-3 предложения
- Верни только текст оценки, без заголовков и пояснений`;
}